from copy import deepcopy
# Исходный список
A = [1, 3, [10, 20], "Python", [40, 50]]
# Создание поверхностной копии списка
B = deepcopy(A)
print("Исходные значения")
print("A:", A)
print("B:", B)
# Внесение изменений в исходный список
A[0] = [100, 200]
A[2][1] = 300
A[3] = "Java"
A[4] = 90
print("После внесения изменений")
print("A:", A)
print("B:", B)
