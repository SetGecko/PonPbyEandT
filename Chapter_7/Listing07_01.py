print("Числа заданы в разных системах")
# Число 19 в двоичной системе
A = 0b10011
print("A =", A)
# Число 93 в восьмеричной системе
B = 0o135
print("B =", B)
# Число 2603 в шестнадцатеричной системе
C = 0xA2B
print("C =", C)
print("C - A - B =", C-A-B)
# Обратное преобразование
print("Обратное преобразование")
A = int("10011", 2)
print(bin(A), "=", A)
B = int("0o135", 8)
print(oct(B), "=", B)
C = int("0xA2B", 16)
print(hex(C), "=", C)
D = int("ABC", 20)
print("ABC =", D)
