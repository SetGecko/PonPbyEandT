# Исходные множества
A = {1, 2}
B = {1, 2, 3}
C = {3, 2, 1}
# Содержимое множеств
print("A =", A)
print("B =", B)
print("C =", C)
# Сравнение множеств
print("A == B:", A == B)
print("A != B:", A != B)
print("B == C:", B == C)
print("B != C:", B != C)
print("A < B:", A < B)
print("A > B:", A > B)
print("B < C:", B < C)
print("B <= C:", B <= C)
print("B >= C:", B >= C)
print("B >= A:", B >= A)
